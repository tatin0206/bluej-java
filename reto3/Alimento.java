public class Alimento
{
    private double gramos;
    
    public Alimento()
    {
    
    }
    
    public Alimento(double gramos)
    {
        this.gramos = gramos;
    }
    public void setGramos (double gramos)
    {
        this.gramos = gramos;
    }
    
    public double getGramos()
    {
        return this.gramos;
    }
}
